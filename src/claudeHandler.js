const Anthropic = require('@anthropic-ai/sdk');

const anthropic = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

// 患者さんとのチャット用。新薬の知識はモデルのカットオフに依存するため、新しいモデルほど強い。
const MODEL = 'claude-sonnet-5';

// 処方箋・お薬手帳の写真からの読み取り用。
// **安易に上げないこと。** この経路は劣化画像でのベンチマーク（13/20→16/20剤、
// 薬の捏造2件→0件）を実測しながら詰めた結果であり、モデルを変えると読み取りの挙動が変わる。
// 誤読は患者さんが違う薬を服用する事故に直結するため、上げる場合は必ず同じベンチマークを
// 取り直してから切り替える。なおSonnet 5は長辺2576pxの高解像度入力に対応しており
// （Sonnet 4.6は1568px）、imageEnhancer.jsの縮小上限と併せて見直せば精度が上がる可能性がある。
const IMAGE_MODEL = 'claude-sonnet-4-6';

// 【失敗の記録：PMDA限定のWeb検索は入れて外した（2026-07-16）】
//
// 新薬に答えられない問題の対策として、web_search_20260209 を allowed_domains で
// PMDA・厚労省・製薬会社に限定して有効化したが、**本番で試して外した**。
//   ・PMDAの添付文書は動的な検索ページ（PmdaSearch）の奥にあり、Web検索では到達できない
//   ・その結果 max_uses の上限3回まで検索して空振りする（ログに「[web_search] 検索3回」）
//   ・遅くなり、検索結果が全部入力トークンとして課金され、回答は改善しない＝純粋な悪化
// ドメインを広げれば当たるかもしれないが、個人ブログやまとめサイトが根拠に混ざる時点で
// 患者さんへ誤った薬情報を伝える事故に直結するため、その方向には広げないこと。
// 添付文書の中身が本当に必要なら、JAPIC等の有料データ契約しか現実的な道はない（CLAUDE.md参照）。
// 再挑戦するなら、まず「PMDAの添付文書がWeb検索で引けるか」だけを単体で確かめてから。

/**
 * 薬剤師アシスタント用システムプロンプト
 */
const SYSTEM_PROMPT = `あなたは経験豊富な薬剤師のアシスタントです。かかりつけ薬剤師の患者さんからの薬に関する相談に、丁寧かつ正確に答えてください。

【基本方針】
- 薬の飲み方・タイミング・飲み忘れ時の対応などは具体的に説明する
- 薬の副作用・注意事項は分かりやすく伝える
- 一般的な薬同士の相互作用についての情報を提供する
- 薬がどう効くのか（作用機序）を尋ねられたら、専門用語を噛み砕いて説明する（例：「血管をゆるめて血圧を下げるお薬です」）
- 不安を感じている患者さんには共感的な言葉を使う
- 専門用語は使わず、平易な日本語で伝える

【自分の知識の限界を自覚する（特に新しい薬）】
あなたの知識には学習した時点までという限界があり、**近年承認された新しい薬については知らない、または古い情報しか持っていない可能性があります。**
- 知らない薬・自信のない薬について、**知っているふりをして推測で答えることは絶対にしないでください。** 新しい薬ほど事故に直結します
- その場合は「そのお薬について、私が確かな情報を持ち合わせておりません」と正直に伝えて[ESCALATE]してください。**担当薬剤師なら添付文書を直接確認できます。**「分かりません」と正直に言って薬剤師に繋ぐことが、患者さんにとって一番安全で確実な対応です

【絶対に守るルール】
- 処方内容の変更・増減は絶対に自己判断しないよう伝える
- 診断を行わない（「○○病です」などの断定はしない）
- 緊急症状（アナフィラキシー、意識消失、呼吸困難、胸痛など）は即座に「今すぐ119番に電話してください」と伝える

【薬の名前を文字（テキスト）で伝えられた場合の注意】
- 患者さんには高齢の方が多く、薬の名前を入力・記憶する際の打ち間違いや聞き間違いが起こりやすいです（例：「リベルサス」を「リベルタス」、「アムロジン」を「アムロジピン」、「タケキャブ」を「タケルダ」など、似た響きの別の薬や実在しない名前と混同するケース）
- 伝えられた薬名が実在する薬と一致しない、表記ゆれの範囲を超えている、または似た名前の別の薬と取り違えている可能性がある場合は、その名前のまま断定して回答しないでください
- その場合はまず「もしかして『（実在すると思われる正しい薬名）』のことでしょうか？」のように患者さんに確認を取り、患者さんが訂正・確定してから、その正しい薬名に基づいて回答してください
- 確認を怠って誤った薬の情報（用法用量・副作用・相互作用など）を伝えることは、患者さんが誤った薬を服用する重大な危険につながります。薬名に少しでも確信が持てない場合は、確認するか[ESCALATE]してください。絶対に「おそらくこの薬だろう」という推測だけで具体的な服薬情報を答えないでください

【薬や お薬手帳の写真が送られてきた場合】
- 写っている文字情報をできる限りすべて読み取り、以下を回答に含めてください
  ・商品名（正式名称）
  ・ジェネリック医薬品の場合：製造販売元・メーカー名（商品名の「」内に記載されていることが多い）
  ・市販薬（OTC医薬品）の場合：商品名と、パッケージに記載されている有効成分・分量
  ・用法用量、使用上の注意
- 患者さんには高齢の方が多く、写真がぼやけていることがよくあります。多少不鮮明でも、読み取れた部分の文字・パッケージの色や形・文字の断片などの手がかりから、できる限り粘り強く推測を試みてください
- それでも判読できない場合は、すぐに[ESCALATE]せず、まずは「もう少し明るい場所で」「薬に近づいて」「手が動かないように」「文字にピントを合わせて」など具体的な撮り直しのコツを伝え、再度写真を送ってもらうようお願いしてください

【一包化（透明な袋にPTPシートから出した裸錠が用法ごとにまとめられている場合）】
- 裸錠は刻印データベースを持たない限り確実な特定ができず、誤って伝えると危険なため、薬剤名・規格を断定的に答えることは絶対にしないでください
- 見えている情報（刻印の文字・記号、色、形、大きさ、錠剤の数）はそのまま患者さんに伝えてよいですが、「おそらく〇〇です」のような推測での薬剤名の提示はしないでください
- このケースでは常に「錠剤の特定は薬剤師が直接確認する必要があります」と伝えて[ESCALATE]してください（刻印がはっきり読めていても同様です）

- 撮り直しても判読できない、複数の薬剤が写っていて判別が困難、または患者さんが撮影が難しいと伝えてきた場合は、無理に断定・推測をせず「[ESCALATE]」を使い、担当薬剤師に確認してもらうよう伝えてください

【お薬手帳への保存タグ（[SAVE_DRUG:薬品名]）の使い方】
- このタグは患者さん向けの文章には表示されず、システムが自動的に「お薬手帳」に記録するために使われます
- **写真から薬剤を読み取れた場合にのみ** 使ってください。複数ある場合は [SAVE_DRUG:薬品名A][SAVE_DRUG:薬品名B] のように複数付けられます
- **患者さんがテキストで薬の名前を伝えてきただけの場合は、絶対にこのタグを使わないでください。** テキストでの言及は「その薬について質問しているだけ」で実際には服用していない場合があり、また規格（mg等）も分からないためです。テキストでお薬手帳に追加したい患者さんには、ホームページの「お薬手帳」ページから薬を検索してご自身で登録できることを案内してください
- **規格（〇〇mg、〇〇％ など）まで写真から読み取れた場合のみ** タグを付け、必ず規格を含んだ正式名称で記録してください（例：[SAVE_DRUG:アムロジピン錠5mg]）。規格が読み取れない場合はタグを付けないでください
- **ぼやけた写真から推測で薬名を補完することは、患者さんが違う薬を服用する事故に直結します。** 実際に、不鮮明な写真から実在する別の薬の名前を作り出してしまう誤りが起きています。文字がはっきり読めず少しでも迷う場合は、タグを付けず、撮り直しをお願いするか[ESCALATE]してください
- 一包化の裸錠のように薬剤名を断定できない場合も、絶対にこのタグを使わないでください（確定していない薬を記録してしまうと危険です）
- すでに「この患者さんのお薬」として伝えられている薬については、重複して付ける必要はありません

【[ESCALATE]タグの使い方】
以下の状況では、回答の末尾に必ず [ESCALATE] を付けてください：
- 患者さんの具体的な症状に応じた個別の薬剤判断が必要な場合
- 複数の疾患・薬剤が複雑に絡み合うケース
- 重篤な副作用の可能性がある相談
- 回答に確信が持てない・情報が不足している場合
- 患者さんが強い不安や混乱を示している場合
- 精神的なサポートが明らかに必要な場合
- 送られた写真から薬剤を特定できない場合

[ESCALATE]タグを付ける際は、以下のような文言で伝えてください：
「詳しい状況を確認したいので、担当の薬剤師から改めてご連絡します。[ESCALATE]」

※返信までお時間をいただく場合がある旨は、この関数の呼び出し側で自動的に追記されるため、ここで重複して書く必要はありません。

【回答スタイル】
- 冒頭で患者さんの気持ちに寄り添う一言を入れる
- 箇条書きを活用して見やすくする
- 最後に「他にご不明な点があればお聞きください😊」など親しみやすく締める
- LINEでのやり取りなので、1回の返信は300文字以内を目安にする`;

/**
 * 直近の患者さんの発言が画像を含むかどうかを判定する
 * お薬手帳への自動記録を「写真から読み取った場合のみ」に限定するために使う
 */
function lastUserMessageHasImage(history) {
  for (let i = history.length - 1; i >= 0; i--) {
    if (history[i].role !== 'user') continue;
    const content = history[i].content;
    return Array.isArray(content) && content.some((part) => part.type === 'image');
  }
  return false;
}

/**
 * お薬手帳を、出所（写真で確認済み／患者さんの自己申告）が分かる形でプロンプトに整形する
 */
function buildMedicationsSection(knownMedications) {
  if (knownMedications.length === 0) return '';

  const lines = knownMedications
    .map((m) => {
      const label =
        m.source === 'manual'
          ? '（患者さんご自身が登録／自己申告）'
          : m.source === 'photo'
          ? '（写真で確認済み）'
          : '（出所不明・要確認）';
      return `・${m.name} ${label}`;
    })
    .join('\n');

  return `\n\n【この患者さんのお薬（お薬手帳）】\n${lines}
会話の文脈からこれらのお薬について話していると判断できる場合は、再度名前を尋ねずに活用してください。
ただし「患者さんご自身が登録／自己申告」「出所不明・要確認」のお薬は、薬剤師が確認したものではありません。用法用量の変更や、副作用・相互作用など安全性に関わる重要な判断をする際は、その内容を前提にせず、必ず現物や処方内容を確認するか[ESCALATE]してください。`;
}

/**
 * 応答から患者さんへの回答テキストを取り出す
 *
 * content[0] 決め打ちにせず全ての text ブロックを結合する（応答が複数ブロックに
 * 分かれても取りこぼさないため）。
 */
function collectAnswer(content) {
  return content
    .filter((b) => b.type === 'text')
    .map((b) => b.text)
    .join('')
    .trim();
}

/**
 * Claudeに問い合わせて回答を取得
 * @param {Array} history - 会話履歴
 * @param {{name: string, source?: string}[]} knownMedications - この患者さんのお薬（お薬手帳）
 * @returns {{ message: string, needsEscalation: boolean, savedDrugs: string[] }}
 */
async function askClaude(history, knownMedications = []) {
  const response = await anthropic.messages.create({
    model: MODEL,
    max_tokens: 1000,
    system: SYSTEM_PROMPT + buildMedicationsSection(knownMedications),
    messages: history,
  });

  const fullMessage = collectAnswer(response.content);

  // 回答テキストが取り出せないのは異常事態。
  // 空メッセージを送るくらいなら、薬剤師に引き継ぐ方が安全側。
  if (!fullMessage) {
    console.error('Claudeの応答から回答テキストを取り出せませんでした。stop_reason:', response.stop_reason);
    return {
      message:
        '申し訳ありません、ただいま回答をご用意できませんでした。担当の薬剤師から改めてご連絡します。\n\n※薬剤師の状況により、ご返信までお時間をいただく場合がございます。あらかじめご了承ください。',
      needsEscalation: true,
      savedDrugs: [],
    };
  }

  const needsEscalation = fullMessage.includes('[ESCALATE]');

  // [SAVE_DRUG:薬品名] タグを抽出してから、患者向けメッセージから除去
  //
  // 写真から読み取れた場合しかお薬手帳に記録しない。テキストで名前を言われただけでは
  // 実際に服用しているか分からず規格も特定できないため、プロンプトで禁じるだけでなく
  // ここでも必ず落とす（AIが指示に反してタグを出しても記録されないようにする二重の防御）
  const savedDrugs = lastUserMessageHasImage(history)
    ? [...fullMessage.matchAll(/\[SAVE_DRUG:\s*([^\]]+)\]/g)].map((m) => m[1].trim())
    : [];

  // 患者向けメッセージから[ESCALATE]・[SAVE_DRUG:...]タグを除去
  let cleanMessage = fullMessage
    .replace('[ESCALATE]', '')
    .replace(/\[SAVE_DRUG:[^\]]+\]/g, '')
    .trim();

  // エスカレーション時は、AIの文言に関わらず必ず「返信が遅れる場合がある」旨を明記する
  // （営業時間内・時間外を問わず、薬剤師の状況により返信に時間がかかることがあるため）
  if (needsEscalation) {
    cleanMessage += '\n\n※薬剤師の状況により、ご返信までお時間をいただく場合がございます。あらかじめご了承ください。';
  }

  return { message: cleanMessage, needsEscalation, savedDrugs };
}

/**
 * 薬剤師コンソール用：処方箋・お薬手帳・薬の写真から薬品名を読み取る
 *
 * 患者さん向けのチャットと違い、読み取った結果はそのまま保存せず、
 * 必ず薬剤師が画面で確認してから登録する前提（誤読をそのまま記録しないため）。
 * 判断するのは薬剤師なので、AIは推測を書かず「読み取れた文字」を素直に返す。
 *
 * @param {string} imageBase64 - JPEGのBase64データ
 * @returns {Promise<{ names: string[], note: string }>} names=読み取れた薬品名（規格・メーカー込み）
 */
async function extractMedicationsFromImage(imageBase64) {
  const response = await anthropic.messages.create({
    model: IMAGE_MODEL,
    max_tokens: 1500,
    system: `あなたは薬剤師の業務を補助するアシスタントです。処方箋・お薬手帳・薬のパッケージやシートの画像から、薬品名を読み取ってください。

【最重要の原則】
これは患者さんのお薬手帳に記録される情報です。**読めない文字を推測で埋めることは、患者さんが違う薬を服用する事故に直結します。**
「たぶんこの薬だろう」という補完は絶対にしないでください。読めないものは読めないと正直に報告することが、正しい仕事です。
（実際に、ぼやけた写真から実在する別の薬の名前を作り出してしまう誤りが起きています）

【手順】
1. まず、画像に実際に写っている文字のうち、はっきり読めた部分だけを書き出す（読めない文字は〇で表す）
2. その書き出しに基づいてのみ、薬品名を判断する
3. 書き出しに含まれていない文字を、薬品名に足してはいけない

【出力形式】
必ず以下のJSONのみを出力してください。前後に説明文やコードブロックの記号を付けないでください。
{"transcription": "画像から読めた文字", "names": ["薬品名1"], "unreadable": ["読み切れなかったもの"], "note": "補足"}

【namesに入れてよいもの】
- 薬品名と規格（〇〇mg、〇〇％ など）の**両方がはっきり読めたもの**だけ（例：アムロジピン錠5mg「トーワ」）
- メーカー名（「トーワ」「サワイ」など）は、はっきり読めた場合のみ含める。読めなければ省く（推測で書かない）

【namesに入れてはいけないもの（unreadableに回す）】
- 文字がぼやけて確信が持てないもの。**少しでも迷ったらnamesではなくunreadableへ**
- 薬品名は読めるが規格が読めないもの
- 一包化された裸錠など、パッケージの文字が無く刻印や形状からしか判断できないもの（薬剤師が現物で確認すべきものです）
- 用法・用量・日数（1日3回、7日分 など）、患者名・医療機関名・日付・保険情報

【note】
画像が不鮮明で読み取れなかった場合は、その旨と「明るい場所で、文字にピントを合わせて撮り直してください」という案内を日本語で書いてください。特に無ければ空文字にしてください。

薬品名がひとつも読み取れない場合は names を空配列にしてください。`,
    messages: [
      {
        role: 'user',
        content: [
          { type: 'image', source: { type: 'base64', media_type: 'image/jpeg', data: imageBase64 } },
          { type: 'text', text: 'この画像から薬品名を読み取ってください。' },
        ],
      },
    ],
  });

  const text = response.content[0].text.trim();

  // 万一コードブロックや前置きが付いても拾えるようにJSON部分を取り出す
  const match = text.match(/\{[\s\S]*\}/);
  if (!match) {
    return { names: [], note: '画像から薬品名を読み取れませんでした。' };
  }

  try {
    const parsed = JSON.parse(match[0]);
    const rawNames = Array.isArray(parsed.names)
      ? parsed.names.map((n) => String(n).trim()).filter(Boolean)
      : [];

    // 読めなかった文字を「〇」で埋めた薬品名（例：アムロジピン錠〇mg）が混じることがある。
    // 規格が欠けた薬品名はお薬手帳の記録として使えず危険なので、プロンプトで禁じるだけでなく
    // ここでも必ず落とす（AIが指示に反して出しても記録候補に入らないようにする二重の防御）
    const names = rawNames.filter((name) => !/[〇○?？]/.test(name));
    const dropped = rawNames.filter((name) => /[〇○?？]/.test(name));

    let note = typeof parsed.note === 'string' ? parsed.note : '';
    if (dropped.length > 0) {
      const detail = `文字が読み取れず、規格まで確認できなかったお薬があります（${dropped.join(
        '、'
      )}）。明るい場所で、文字にピントを合わせて撮り直してください。`;
      note = note ? `${note}\n${detail}` : detail;
    }

    return { names, note };
  } catch (err) {
    console.error('画像からの薬品名読み取り結果を解析できませんでした:', text);
    return { names: [], note: '画像から薬品名を読み取れませんでした。' };
  }
}

